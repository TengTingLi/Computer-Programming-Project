    public void graph_plot_gt(){
        double a1 = Double.parseDouble(tf_a1.getText());
        double f1 = Double.parseDouble(tf_f1.getText());
        double a2 = Double.parseDouble(tf_a2.getText());
        double f2 = Double.parseDouble(tf_f2.getText());
        y_max = find_y_max(a1, a2);
        graph_initialize();
        for(double t = 0; t < 1; t += 0.0001){
            double g1t = a1 * Math.sin(2 * Math.PI * f1 * t);
            double g2t = a2 * Math.sin(2 * Math.PI * f2 * t);
            graph_plot(t, result(g1t, g2t));
            if(y_max_1st < result(g1t, g2t)){
                    y_max_1st = result(g1t, g2t);
                    x_max_1st = t;
            }
            if(y_min_1st > result(g1t, g2t)){
                    y_min_1st = result(g1t, g2t);
                    x_min_1st = t;
            }
        }
        draw_min_max();
    }
    
    public double result(double a, double b){
        double c = 0;
        String chk = (String)cb_operation.getSelectedItem();
        switch(chk){
            case "+" -> c = a + b;
            case "-" -> c = a - b;
            case "*" -> c = a * b;
            case "/" -> c = a / b;
        }
        return c;
    }
    
    public double find_y_max(double a, double b){
        double c = a >= b ? a : b;
        double d = 0;
        String chk = (String)cb_operation.getSelectedItem();
        switch(chk){
            case "+" -> d = result(a, b) >= c ? result(a, b) : c;
            case "-" -> d = a + b >= c ? a + b : c;
            case "*" -> d = result(a, b) >= c ? result(a, b) : c;
            case "/" -> d = result(a, b) >= c ? result(a, b) : c;
        }
        return d;
    }
    
