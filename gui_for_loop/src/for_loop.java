    Graphics drawer;
    double y_max; 
    double y_max_1st, y_min_1st, x_max_1st, x_min_1st;
    int w, h;
    int plot_x_previous, plot_y_previous;
    
    public void graph_initialize(){     //draw everything to the graph
        //set initial value -> draw axis line -> draw axis scale
        drawer = panel_graph.getGraphics();
        drawer.setColor(Color.white);
        w = panel_graph.getWidth();
        h = panel_graph.getHeight();
        y_max_1st = y_min_1st = x_max_1st = x_min_1st = 0;
        plot_x_previous = 10;
        plot_y_previous = h/2;
        drawer.fillRect(0, 0, w, h);
        drawer.setColor(Color.black);
        drawer.drawLine(0, h/2, w, h/2);    //draw x-axis 
        drawer.drawLine(10, 0, 10, h);         //draw y-axis
        //draw y scale according to value of y_max
        drawer.drawString(String.valueOf(y_max), 15, 10);
        drawer.drawString(String.valueOf(-y_max), 15, h - 10);
        drawer.drawString(String.valueOf(y_max / 2), 15, (int)((h / 4) + 5));
        drawer.drawString(String.valueOf(-y_max / 2), 15, (int)((h * 3 / 4) - 5));
        //draw x scale
        boolean keep_drawing = true;
        double x_string_position = 10;
        double x_string_value = 0;
        while(keep_drawing){
            drawer.drawString(String.format("%.1f",x_string_value), (int)x_string_position, h/2);
            x_string_position += (w-15)/10;
            x_string_value += 0.1;
            keep_drawing = x_string_value < 1;
        }
        
    }
    
    public void graph_plot(double x, double y){
        int x_at_0 = 10;    //set the (0,0) point
        int y_at_0 = h / 2;
        int plot_x = x_at_0 + (int)(x * (w-15));
        int plot_y = y_at_0 - (int)(y / y_max * ((h - 20) / 2));
        //draw value on y-axis
        drawer.drawLine(plot_x, plot_y, plot_x_previous, plot_y_previous);
        plot_x_previous = plot_x;
        plot_y_previous = plot_y;
        
    }
    
    public void draw_min_max(){
        drawer.setColor(Color.blue);
        drawer.fillOval(7 + (int)(x_max_1st * (w-15)), h / 2 - 2 - (int)(y_max_1st / y_max * ((h - 20) / 2)), 5, 5);
        drawer.fillOval(7 + (int)(x_min_1st * (w-15)), h / 2 - 2 - (int)(y_min_1st / y_max * ((h - 20) / 2)), 5, 5);
        if(x_max_1st < 0.5){
            drawer.drawString(String.format("Maximum point(%.2f, %.2f)", x_max_1st, y_max_1st),
                                           7 + (int)(x_max_1st * (w-15)),
                                           h / 2 - (int)(y_max_1st / y_max * ((h - 20) / 2)));
        }
        if(x_min_1st < 0.5){
            drawer.drawString(String.format("Minimum point(%.2f, %.2f)", x_min_1st, y_min_1st),
                                           7 + (int)(x_min_1st * (w-15)),
                                           h / 2 - 2 - (int)(y_min_1st / y_max * ((h - 20) / 2)));
        }
        if(x_max_1st > 0.5){
            drawer.drawString(String.format("Maximum point(%.2f, %.2f)", x_max_1st, y_max_1st),
                                           -150 + (int)(x_max_1st * (w-15)),
                                           h / 2 - (int)(y_max_1st / y_max * ((h - 20) / 2)));
        }
        if(x_min_1st > 0.5){
            drawer.drawString(String.format("Minimum point(%.2f, %.2f)", x_min_1st, y_min_1st),
                                           -150 + (int)(x_min_1st * (w-15)),
                                           h / 2 - 2 - (int)(y_min_1st / y_max * ((h - 20) / 2)));
        }
    }
    
    public void graph_plot_g1t(){ //get value from gui -> initial graph -> plot graph
        double a1 = Double.parseDouble(tf_a1.getText());
        double f1 = Double.parseDouble(tf_f1.getText());
        y_max = a1;
        graph_initialize();
        for(double t = 0; t < 1; t += 0.0001){
            double g1t = a1 * Math.sin(2 * Math.PI * f1 * t);
            graph_plot(t, g1t);
            if(y_max_1st < g1t){
                    y_max_1st = g1t;
                    x_max_1st = t;
            }
            if(y_min_1st > g1t){
                    y_min_1st = g1t;
                    x_min_1st = t;
            }
        }
        draw_min_max();
    }
    
